// 第一步：相加各位的值，不算进位，二进制每位相加就相当于各位做异或操作；
// 第二步：计算进位值，相当于各位做与操作，再向左移一位。
// 第三步重复上述两步， 各位相加 ，计算进位值。进位值为0，跳出循环。


