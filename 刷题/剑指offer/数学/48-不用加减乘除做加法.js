function Add(num1, num2)
{
  var sum, carry;
  do {
    sum = num1 ^ num2; //不带进位的加法
    carry = (num1 & num2) << 1; //进位
    num1 = sum;
    num2 = carry;
  } while (num2 !== 0); // 进位不为0则继续执行加法处理进位
  return num1;
}
